"""
range([start], stop[, step])
"""

print("range(5):")
for i in range(5): 
    print(i, end=" ")

print("\nrange(2, 10):")
for i in range(2, 10):
    print(i, end=" ")

print("\nrange(2, 20, 2):")
for i in range(2, 20, 2):
    print(i, end=" ")

print("\nrange(20, 0, -1):")
for i in range(20, 0, -1):
    print(i, end=" ")
